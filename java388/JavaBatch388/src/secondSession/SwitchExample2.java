package secondSession;

public class SwitchExample2 {

	public static void main(String[] args) {
		int row = 4;
		//Rows in theater
		switch (row) {
		case 4:
			System.out.println("Goodie for Row 4");
		
		case 3:
			System.out.println("Goodie for Row 3");
		
		case 2:
			System.out.println("Goodie for Row 2");
		
		case 1:
			System.out.println("Goodie for Row 1");
		
		}
	}

}
